  <div class="ingredients-list-div grid-area">
    <h3 class="title recipe-ing">Ingredients</h3>
    <ul>
      <li>4 Flour Tortillas</li>
      <li>3 oz Radishes</li>
      <li>1 Orange</li>
      <li>¾ lb Yukon Gold Potatoes</li>
      <li>6 oz Cremini Mushrooms</li>
      <li>1 Shallot</li>
      <li>3 Tbsps Roasted Peanuts</li>
      <li>¼ cup Sour Cream</li>
      <li>1 oz Sliced Pickled Jalapeño Pepper</li>
      <li>1 oz Golden Or Red Sweet Piquante Peppers</li>
      <li>1 Tbsp Spanish Spice Blend</li>
      <li>1 Romaine Lettuce Heart</li>
      <li>2 Tbsps Rice Vinegar</li>
    </ul>
  </div>
  <div class="pic-step-combo-1 grid-area card-step">
    <img class="card-img later-recipe-img recipe-step-img card-img" src="img/example-recipe/1225_2PV2_Chorizo-Tacos_00103_WEB_retina_feature.jpg">
    <div class="container-step">
      <h4 class="title recipe-steps">1  Prepare & roast the vegetables</h4>
      <p>Preheat the oven to 450°F. Wash and dry the fresh produce. Quarter the mushrooms. Quarter the potatoes lengthwise, then cut crosswise into 1/2- inch pieces. Place the vegetables on a sheet pan. Drizzle with olive oil and season with salt, pepper, and 3/4 of the spice blend; toss to coat. Arrange in an even layer. Roast 21 to 23 minutes, or until browned and tender when pierced with a fork. Remove from the oven.</p>
    </div>
  </div>
  <div class="pic-step-combo-2 grid-area card-step">
    <img class="card-img later-recipe-img recipe-step-img card-img" src="img/example-recipe/1225_2PV2_Mushroom-Potato-Tacos_18402_WEB_retina_feature.jpg">
    <div class="container-step">
      <h4 class="title recipe-steps">2  Prepare the remaining ingredients</h4>
      <p>While the vegetables roast, roughly chop the piquante peppers. Peel and small dice the shallot. Cut off and discard the root end of the lettuce; roughly chop the leaves. Peel and medium dice the orange. Cut off and discard the ends of the radishes; halve lengthwise, then thinly slice crosswise. Roughly chop the jalapeño pepper; thoroughly wash your hands and cutting board immediately after handling. In a bowl, combine the sour cream, a drizzle of olive oil, and as much of the remaining spice blend as you’d like (you may have extra). Season with salt and pepper to taste.</p>
    </div>
  </div>
  <div class="pic-step-combo-3 grid-area card-step">
    <img class="card-img later-recipe-img recipe-step-img card-img" src="img/example-recipe/1225_2PV2_Chorizo-Tacos_00125_WEB_retina_feature.jpg">
    <div class="container-step">
      <h4 class="title recipe-steps">3  Warm the tortillas</h4>
      <p>While the vegetables continue to roast, place the tortillas on a large piece of aluminum foil. Tightly wrap to seal. Place directly onto the oven rack and warm 5 to 7 minutes, or until heated through and pliable. Transfer to a work surface and carefully unwrap.</p>
    </div>
  </div>
  <div class="pic-step-combo-4 grid-area card-step">
    <img class="card-img later-recipe-img recipe-step-img card-img" src="img/example-recipe/1225_2PV2_Mushroom-Potato-Tacos_18407_WEB_retina_feature.jpg">
    <div class="container-step">
      <h4 class="title recipe-steps">4  Make the pepper relish</h4>
      <p>While the tortillas warm, in a medium bowl, combine the chopped piquante peppers, diced shallot, half the vinegar, and as much of the chopped jalapeño pepper as you’d like, depending on how spicy you’d like the dish to be. Drizzle with olive oil and season with salt and pepper to taste.</p>
    </div>
  </div>
  <div class="pic-step-combo-5 grid-area card-step">
    <img class="card-img later-recipe-img recipe-step-img card-img" src="img/example-recipe/1225_2PV2_Mushroom-Potato-Tacos_18434_WEB_retina_feature.jpg">
    <div class="container-step">
      <h4 class="title recipe-steps">5  Finish the salad</h4>
      <p>While the tortillas continue to warm, in a medium bowl, combine the chopped lettuce, diced orange, sliced radishes, peanuts, and remaining vinegar. Drizzle with olive oil and season with salt and pepper to taste.</p>
    </div>
  </div>
  <div class="pic-step-combo-6 grid-area card-step">
    <img class="card-img later-recipe-img recipe-step-img card-img" src="img/example-recipe/1225_2PV2_Mushroom-Potato-Tacos_18424_WEB_retina_feature.jpg">
    <div class="container-step">
      <h4 class="title recipe-steps">6  Assemble the tacos & serve your dish</h4>
      <p id="last-step">Spread a layer of the seasoned sour cream onto the warmed tortillas. Top with the roasted vegetables and pepper relish. Serve the tacos with the salad on the side. Enjoy!</p>
